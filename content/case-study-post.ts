// Content for the "פוסט קייס סטאדי" tool (chapter 7).
// The four-part structure is taught on camera; the prompt turns one real story
// from the learner's work into a post, and closes it without begging.

export const CASE_STUDY_INTRO =
  "לכל אחד יש סיפור אחד מהעבודה ששווה יותר ממה שנדמה לו. לקוח שכמעט עזב וחזר, תהליך שנשבר ותוקן, החלטה שהייתם בטוחים בה וטעיתם. בפרופיל הסיפור הזה צר מדי, כי שדה שנסרק צריך מספרים והיקף. בפוסט הוא בדיוק מה שגורם לאנשים לזכור אתכם. הפרומפט הזה לוקח סיפור אחד אמיתי ובונה ממנו פוסט בארבעה חלקים: מה היה המצב, מה עשיתם שלב אחרי שלב, מה יצא מזה, ומה הייתם עושים אחרת. ובסוף, משפט אחד ענייני שאומר שאתם מחפשים, בלי להפוך את כל הפוסט לבקשה.";

export const CASE_STUDY_PROMPT = `אתה עורך תוכן שמתמחה בפוסטים מקצועיים בלינקדאין. המשימה: לקחת סיפור אחד אמיתי מהעבודה שלי ולהפוך אותו לפוסט.

🛑 חשוב: אל תכתוב את הפוסט עדיין. קודם תשאל אותי, שאלה אחת בכל פעם, ותחכה לתשובה שלי לפני שאתה ממשיך.

== שלב 1: תחקיר (שאלה אחת בכל פעם) ==
1. מה הסיפור, בשתי שורות? מה קרה ומתי.
2. מה היה המצב בהתחלה? בלי לייפות. מה היה שבור, תקוע או מסוכן.
3. מה עשית בפועל, שלב אחרי שלב? ספר לי גם מה ניסית שלא עבד. זה החלק שאנשים הכי זוכרים, ובלעדיו הפוסט נשמע כמו הצלחה מלוטשת שאף אחד לא מאמין לה.
4. מה יצא מזה? מספר אם יש לך. אם אין מספר מדויק, תן טווח או תיאור היקף. אל תמציא מספר.
5. מה היית עושה אחרת היום? זה מה שמבדיל בין מי שבאמת עשה את זה לבין מי שקרא על זה.
6. מה התפקיד שאתה מחפש עכשיו, ובאיזה סוג חברות? (לצורך המשפט האחרון בלבד)
7. עברית או אנגלית?

== שלב 2: כתיבת הפוסט ==
אחרי שענית על הכל, כתוב פוסט אחד לפי המבנה:

- **פתיחה:** המצב ההתחלתי, במשפט או שניים. בלי הקדמה כללית על התחום, ישר לתוך המצב.
- **מה עשיתי:** שלב אחרי שלב, בגוף ראשון, כולל מה שלא עבד.
- **מה יצא:** התוצאה, עם המספר או ההיקף שנתתי.
- **מה הייתי עושה אחרת:** משפט או שניים של כנות אמיתית.
- **סגירה:** משפט אחד ענייני על מה שאני מחפש. למשל: "אגב, אני בדיוק מחפשת תפקיד X בחברות Y. אם עולה משהו אצלכם, אשמח שתחשבו עליי."

== כללים קשיחים ==
- זה "ככה אני עשיתי", לא "ככה עושים". בלי רשימת טיפים, בלי הכללות, בלי לימוד. סיפור אחד, שלי.
- אסור להמציא שום פרט, מספר, ציטוט או תוצאה שלא נתתי לך. אם חסר לך משהו, תשאל.
- הסגירה היא משפט אחד בלבד. אסור סימני קריאה, אסור "אשמח לשיתוף", ואסור להפוך את הפוסט לבקשה.
- בלי אימוג'ים בגוף הפוסט. לכל היותר אחד בסגירה, ורק אם זה טבעי.
- בלי מקף ארוך (— או –). פסיק, נקודה או סוגריים.
- אסור "זה לא X אלא Y", ואסור לפתוח משפט בשלילה.
- בלי סופרלטיבים: מדהים, מטורף, פורץ דרך, משנה חיים.
- משפטים באורך משתנה. אסור רצף של משפטים קצרים באותו אורך, זה נשמע כמו טקסט שנכתב במכונה.
- אורך: 120 עד 200 מילים. אם יצא ארוך יותר, תקצר.

== ואז ==
תראה לי את הפוסט, ותשאל אותי מה לחדד. אם משהו בו לא נשמע כמוני, אני אגיד לך ונתקן יחד.

הערה אחרונה: לינקדאין מדחיקה תוכן AI גנרי מההמלצות, כלומר החברים שלי יראו אותו וזרים לא. לכן הפוסט חייב להישאר בקול שלי ועם הפרטים הספציפיים שלי, ולא להפוך לטקסט חלק שאפשר להדביק על כל אחד.`;
